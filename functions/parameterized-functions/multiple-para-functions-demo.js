function addTwoNumbers(num1, num2) {
    console.log(num1 + num2);
}
function multiplyTwoNumbers(num1, num2) {
    console.log(num1 * num2);
}
function threeValues(num1, num2, string) {
    console.log(num1 + num2 + string);
}

addTwoNumbers(5, 10); // Output: 15
multiplyTwoNumbers(5, 10); // Output: 50
threeValues(5, 10, " is the result."); // Output: 15 is the result.