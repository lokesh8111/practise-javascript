function multipleStatements() {
    console.log("Statement 1");
    console.log("Statement 2");
    console.log("Statement 3");
}
multipleStatements();

// Arrow function version of the above function

const multipleStatementsArrow = () => {
    console.log("Statement 1");
    console.log("Statement 2");
    console.log("Statement 3");
}
multipleStatementsArrow();

// In this example, we have an arrow function with a block body that contains multiple statements. 
// We use curly braces to define the block body, and we can include as many statements as needed.